// 자바는 UTF-16 코드를 지원한다. 한글을 지원한다. 
public class 현병은 {

	public static void main(String args[]) {
		System.out.println("현병은.main() 함수 시작 >>> : ");
		// 운동하는 현병은 : 마라톤 : 부상중 : 재활훈련 : 계단훈련 
		// 도시락싸는 현병은 : 금요일 : 연어사께동
		// 강사 현병은 : 자바기초 중에서 new 연산자 가르킨다.
		// 친구 만나는 현병은 : 카페 커피 마시기 

		// 객체 : Object : 소프트웨어 공학 용어이다.

		현병은 운동 = new 현병은();
		System.out.println("운동 >>> : " + 운동);
		현병은 도시락 = new 현병은();
		System.out.println("도시락 >>> : " + 도시락);
		현병은 강사 = new 현병은();
		System.out.println("강사 >>> : " + 강사);
		현병은 친구 = new 현병은();
		System.out.println("친구 >>> : " + 친구);
	}
} // end of 현병은 클래스 